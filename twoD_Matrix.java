package ASSIGNMENTS.MULTIDIMENSIONAL_ARRAY;

public class twoD_Matrix {
    public static void main(String[] args) {
        int[][] arr=new int[5][5];
        int n=arr.length;
        int m=arr[0].length;
        for(int i=0;i<n;i++){
            for(int j=0;j<m;j++){
                arr[i][j]=10;
            }
        }
        for(int i=0;i<n;i++){
            for(int j=0;j<m;j++){
                System.out.print(arr[i][j]+" ");
            }
            System.out.println();
        }
    }
}
